x = 10
y = 3

print(x + y) #Penjumlahan
print(x - y) #Pengurangan
print(x * y) #Perkalian
print(x ** y) #Perpangkatan
print(x / y) #Pembagian
print(x // y) #Pembagian Pembulatan
print(x % y) #Sisa Pembagian (Modulo)
x += 2
print(x)
y += 3
print(y) 

# Hierarki Matematika (Prioritas)
# 1. Tanda Kurung
# 2. Per Pangkatan
# 3. Perkalian Dan Pembagian
# 4. Penjumlahan Dan Pengurangan